#!/usr/bin/env node
// generate-llms-full.mjs — builds llms-full.txt from the static HTML pages.
// Read each .html file's body, strip nav/footer/scripts/styles, decode entities,
// concatenate with URL markers. Output ~50-200KB depending on blog size.
//
// Run after content changes: node scripts/generate-llms-full.mjs
// Then Vercel auto-deploys the new file at /llms-full.txt.

import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(".");
const SITE_URL = "https://www.inklockautomation.com";
const OUT_PATH = path.join(ROOT, "llms-full.txt");

function findHtmlFiles(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (["node_modules", ".git", "api", "build", "assets", "fonts", "scripts"].includes(entry.name)) continue;
      findHtmlFiles(full, files);
    } else if (entry.name.endsWith(".html") && entry.name !== "404.html") {
      files.push(full);
    }
  }
  return files;
}

function urlFromFilename(rel) {
  let p = rel.replace(/\.html$/, "");
  if (p === "index") return "/";
  if (p.endsWith("/index")) p = p.slice(0, -"/index".length);
  return "/" + p;
}

function titleFromHtml(html) {
  return html.match(/<title>([^<]*)<\/title>/)?.[1] ?? "";
}

function extractReadable(html) {
  let text = html
    .replace(/<!--[\s\S]*?-->/g, "")
    .replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, "")
    .replace(/<style\b[^>]*>[\s\S]*?<\/style>/gi, "")
    .replace(/<noscript\b[^>]*>[\s\S]*?<\/noscript>/gi, "");
  const bodyMatch = text.match(/<body[^>]*>([\s\S]*?)<\/body>/i);
  text = bodyMatch ? bodyMatch[1] : text;
  text = text.replace(/<nav\b[^>]*>[\s\S]*?<\/nav>/gi, "");
  text = text.replace(/<footer\b[^>]*>[\s\S]*?<\/footer>/gi, "");
  text = text.replace(/<header\b[^>]*>[\s\S]*?<\/header>/gi, "");
  text = text.replace(/<h([1-6])\b[^>]*>([\s\S]*?)<\/h\1>/gi, (_m, level, content) => {
    const cleaned = content.replace(/<[^>]+>/g, "").trim();
    if (!cleaned) return "";
    return `\n\n${"#".repeat(parseInt(level))} ${cleaned}\n\n`;
  });
  text = text.replace(/<br\s*\/?>/gi, "\n");
  text = text.replace(/<\/(p|li|div|section|article)>/gi, "\n");
  text = text.replace(/<li\b[^>]*>/gi, "- ");
  text = text.replace(/<[^>]+>/g, "");
  text = text
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#x27;/g, "'")
    .replace(/&#39;/g, "'")
    .replace(/&mdash;/g, "—")
    .replace(/&ndash;/g, "–")
    .replace(/&hellip;/g, "…")
    .replace(/&nbsp;/g, " ");
  return text.replace(/\n{3,}/g, "\n\n").replace(/[ \t]+/g, " ").trim();
}

const sections = [];
const files = findHtmlFiles(ROOT).sort();
for (const f of files) {
  const rel = path.relative(ROOT, f);
  const html = fs.readFileSync(f, "utf8");
  const title = titleFromHtml(html) || rel;
  const body = extractReadable(html);
  if (body.length < 80) continue;
  const fullUrl = SITE_URL + urlFromFilename(rel);
  sections.push(`# ${title}\n\n${fullUrl}\n\n${body}\n`);
}

const header = `# InkLock Automation — Full Content Index

> Concatenated content of every public route on inklockautomation.com.
> Generated by scripts/generate-llms-full.mjs.
> Last built: ${new Date().toISOString()}.
> ${sections.length} pages included.

`;

fs.writeFileSync(OUT_PATH, header + sections.join("\n---\n\n"));
console.log(`Wrote ${OUT_PATH} with ${sections.length} pages, ${(fs.statSync(OUT_PATH).size / 1024).toFixed(1)} KB.`);
